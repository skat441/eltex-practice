int sum(int a, int b){
    int res=a+b;
    return res;
}

int neg(int a, int b){
    int res=a-b;
    return res;
}

int mult(int a, int b){
    int res=a*b;
    return res;
}

double div(double a, double b){
    double res=a/b;
    return res;
}

int mod(int  a, int  b){
    double res=a%b;
    return res;
}
